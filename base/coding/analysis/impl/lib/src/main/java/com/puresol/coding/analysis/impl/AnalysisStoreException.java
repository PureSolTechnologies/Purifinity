package com.puresol.coding.analysis.impl;

public class AnalysisStoreException extends Exception {

    private static final long serialVersionUID = -5082132187888243921L;

    public AnalysisStoreException() {
    }

    public AnalysisStoreException(String message) {
	super(message);
    }

    public AnalysisStoreException(Throwable cause) {
	super(cause);
    }

    public AnalysisStoreException(String message, Throwable cause) {
	super(message, cause);
    }

}
