package com.puresoltechnologies.parsers.ust.eval;

public class UniversalSyntaxTreeEvaluationException extends Exception {

	private static final long serialVersionUID = 5524342865833756305L;

	public UniversalSyntaxTreeEvaluationException() {
		super();
	}

	public UniversalSyntaxTreeEvaluationException(String message,
			Throwable cause) {
		super(message, cause);
	}

	public UniversalSyntaxTreeEvaluationException(String message) {
		super(message);
	}

	public UniversalSyntaxTreeEvaluationException(Throwable cause) {
		super(cause);
	}

}
