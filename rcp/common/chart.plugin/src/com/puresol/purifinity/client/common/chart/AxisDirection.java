package com.puresol.purifinity.client.common.chart;

/**
 * This enum specifies the axis direction.
 * 
 * @author Rick-Rainer Ludwig
 * 
 */
public enum AxisDirection {

	/**
	 * This is the normal X axis which is placed on the lower border of the
	 * plot.
	 */
	X, //
	/**
	 * This is the normal Y axis which is placed on the left border of the plot.
	 */
	Y, //
	/**
	 * This is the alternative X axis which is placed on the upper border of the
	 * plot.
	 */
	ALT_X, //
	/**
	 * This is the alternative Y axis which is placed on the right border of the
	 * plot.
	 */
	ALT_Y, //
	;

}
